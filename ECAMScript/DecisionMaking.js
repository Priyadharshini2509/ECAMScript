//if
var x=10;
if(x<20){
    console.log(x);
}

//if-else
var y=20;
if(x=10){
    console.log(x);
}
else{
    console.log(y);
}

//nested-if
if(x=20){
    if(y=20){
        console.log("Hi");
    }
    else{
        console.log("Hello");
    }
}

//if else if
